package variable;

public class Var2 {

    //메인함수
    public static void main(String[] args) {
        //변수를 선언 - 변수의 이름은 a
        int a;
        //값을 넣다, 값을 초기화 했다
        a = 10;
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);

        System.out.println();
        //변수의 선언과 동시에 초기화
        int b = 20;
        System.out.println(b);
        System.out.println(b);
        System.out.println(b);

    }//end of main

}//end of class
